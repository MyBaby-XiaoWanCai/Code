#define PCA9555_POWER_ADDR             (0x40)

#define PCA9555_INPUT0_REG             (0x00)	//输入寄存器配置地址
#define PCA9555_INPUT1_REG             (0x01)
#define PCA9555_OUTPUT0_REG            (0x02)	//输出寄存器配置地址
#define PCA9555_OUTPUT1_REG            (0x03)
#define PCA9555_POLALN0_REG            (0x04)	//IO取反
#define PCA9555_POLALN1_REG            (0x05)
#define PCA9555_CONFIG0_REG            (0x06)	//寄存器配置地址
#define PCA9555_CONFIG1_REG            (0x07)

#define CurrOkErr					   (0x40)	 //电流故障电流OK地址
