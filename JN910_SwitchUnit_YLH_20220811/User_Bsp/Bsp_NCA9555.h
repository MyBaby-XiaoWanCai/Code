#define PCA_WRITE						(0)		 //写寄存器命令 NCA9555 bit0 位
#define PCA_READ						(1)		 //读寄存器命令

/**********************************************************************************************************
**                                           类型定义区
**********************************************************************************************************/
typedef enum
{
	_OK = 0x00,
	_ERR = 0x01,
}StatusType; //状态信息


